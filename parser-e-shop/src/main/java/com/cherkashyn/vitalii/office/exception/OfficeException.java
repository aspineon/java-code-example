package com.cherkashyn.vitalii.office.exception;

public class OfficeException extends Exception{

	private static final long serialVersionUID = 1L;

	public OfficeException() {
		super();
	}

	public OfficeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public OfficeException(String arg0) {
		super(arg0);
	}

	public OfficeException(Throwable arg0) {
		super(arg0);
	}
	
}
